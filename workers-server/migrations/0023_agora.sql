-- 정책:
--   1) "퀀타리움 지갑주소 = Universal User ID" 원칙 유지.
--      Agora UID 는 wallet_address 의 SHA-256 → 32bit unsigned int
--      로 결정론적(deterministic)으로 파생한다. 클라이언트와 서버
--      양쪽 모두 같은 알고리즘으로 계산하므로 별도 저장 없이도
--      재구성 가능하지만, 조회/디버깅 편의를 위해 컬럼에 캐싱한다.
--
--   2) FCM/APNs 토큰 컬럼은 1차에서는 만들지 않는다 (사장님 결정 C).
--      3차(백그라운드 통화) 단계에서 별도 마이그레이션으로 추가.
--
--   3) 큐알쳇과 같은 Agora App ID 를 공유하지만, 채널명에
--      'eggplant_' prefix 를 강제하여 네임스페이스를 분리한다
--      (서버 토큰 발급 라우트에서 검증).
-- ============================================================

ALTER TABLE users ADD COLUMN agora_uid INTEGER;

-- 같은 지갑주소면 같은 UID. 충돌은 SHA-256 32bit 절단이라 사실상 0
-- (4.2억분의 1). 충돌 시에는 회원가입을 거부하지 않고, 그냥 다음
-- 사용자가 추가 솔트로 재계산하도록 서버에서 처리한다.
CREATE INDEX IF NOT EXISTS idx_users_agora_uid ON users(agora_uid);
