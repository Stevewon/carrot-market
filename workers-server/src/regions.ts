/**
 * 동네 중심점 좌표 lookup.
 *
 * 사생활 보호: 사용자의 정확한 GPS 는 서버에 저장하지 않는다.
 * GPS 는 인증 시점에만 사용해 "이 region 의 중심점에서 RADIUS_KM 안에 있는가" 만 본다.
 * 검증 통과 시 DB 에는 region 중심 좌표(아래 표에 정의된 값)만 저장한다.
 *
 * 좌표는 각 시·구청 위치(공식 행정 중심)에 가깝게 잡았다.
 * 누락된 region 은 검증 자체가 불가하므로 "동네 인증" 버튼이 안 보이고
 * 기존처럼 텍스트 region 만 사용된다.
 */

export interface RegionCenter {
  lat: number;
  lng: number;
}

/** 인증 시 사용자가 region 중심점에서 이만큼 이내여야 통과. */
export const REGION_VERIFY_RADIUS_KM = 4;

/** Haversine 거리 (km). */
export function haversineKm(a: RegionCenter, b: RegionCenter): number {
  const R = 6371;
  const toRad = (d: number) => (d * Math.PI) / 180;
  const dLat = toRad(b.lat - a.lat);
  const dLng = toRad(b.lng - a.lng);
  const lat1 = toRad(a.lat);
  const lat2 = toRad(b.lat);
  const h =
    Math.sin(dLat / 2) ** 2 +
    Math.sin(dLng / 2) ** 2 * Math.cos(lat1) * Math.cos(lat2);
  return 2 * R * Math.asin(Math.min(1, Math.sqrt(h)));
}

